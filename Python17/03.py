n = ["1", "4", "5", "3", "2"]  #создаём массив
n.sort()  #сортируем массив при помощи метода sort.
print(n)  #печатаем наш массив

n1 = ["1", "4", "5", "3", "2"]  #создаём массив №2
n2 = sorted(n1)  #сортируем массив и присваиваем новую переменную
print(n1)  #выводим массив №2 без функции sorted
print(n2)  #выводим  массив №2 после использования функции sorted